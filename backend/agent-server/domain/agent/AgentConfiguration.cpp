bool AgentConfiguration::setExpertTools(const std::string &name,
                                        const std::vector<std::string> &tools) {
  std::lock_guard<std::mutex> lock(mutex_);
  for (auto &e : experts_) {
    if (e.name == name) {
      e.visibleTools = tools;
      rebuildRawJson();
      return true;
    }
  }
  return false;
}

bool AgentConfiguration::setExpertRoutes(const std::string &name,
                                         const std::vector<RouteRule> &routes) {
  std::lock_guard<std::mutex> lock(mutex_);
  for (auto &e : experts_) {
    if (e.name == name) {
      e.nextRules = routes;
      rebuildRawJson();
      return true;
    }
  }
  return false;
}

bool AgentConfiguration::addExpertRoute(const std::string &name,
                                        const RouteRule &rule) {
  std::lock_guard<std::mutex> lock(mutex_);
  for (auto &e : experts_) {
    if (e.name == name) {
      e.nextRules.push_back(rule);
      rebuildRawJson();
      return true;
    }
  }
  return false;
}

bool AgentConfiguration::removeExpertRoute(const std::string &name, int index) {
  std::lock_guard<std::mutex> lock(mutex_);
  for (auto &e : experts_) {
    if (e.name == name) {
      if (index < 0 || static_cast<size_t>(index) >= e.nextRules.size())
        return false;
      e.nextRules.erase(e.nextRules.begin() + index);
      rebuildRawJson();
      return true;
    }
  }
  return false;
}

bool AgentConfiguration::setExpertLlm(const std::string &name,
                                      const std::string &provider,
                                      const std::string &model, int timeout,
                                      double temperature) {
  std::lock_guard<std::mutex> lock(mutex_);
  for (auto &e : experts_) {
    if (e.name == name) {
      e.llmProvider = provider;
      e.llmModel = model;
      e.llmTimeout = timeout;
      e.llmTemperature = temperature;
      rebuildRawJson();
      return true;
    }
  }
  return false;
}


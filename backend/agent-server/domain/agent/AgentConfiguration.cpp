using json = nlohmann::json;

AgentConfiguration &AgentConfiguration::getInstance() {
  static AgentConfiguration instance;
  return instance;
}

bool AgentConfiguration::init(const std::string &configPath) {
  configPath_ = configPath;
  return reconfigure();
}

bool AgentConfiguration::reconfigure() {
  std::lock_guard<std::mutex> lock(mutex_);

  std::ifstream file(configPath_);
  if (!file.is_open())
    return false;

  std::stringstream buf;
  buf << file.rdbuf();
  rawJson_ = buf.str();

  try {
    json j = json::parse(rawJson_);
    experts_.clear();

    if (j.contains("experts") && j["experts"].is_array()) {
      for (const auto &e : j["experts"]) {
        experts_.push_back(ExpertConfig::fromJson(e));
      }
    } else if (j.is_array()) {
      for (const auto &e : j) {
        experts_.push_back(ExpertConfig::fromJson(e));
      }
    }

    // 如果 JSON 有顶层 llm_provider 等字段，注入到每个 Expert（全局默认降级）
    std::string globalProvider = j.value("llm_provider", "");
    std::string globalModel = j.value("llm_model", "");
    int globalTimeout = j.value("llm_timeout", 0);
    double globalTemperature = j.value("llm_temperature", -1.0);

    for (auto &expert : experts_) {
      if (expert.llmProvider.empty() && !globalProvider.empty())
        expert.llmProvider = globalProvider;
      if (expert.llmModel.empty() && !globalModel.empty())
        expert.llmModel = globalModel;
      if (expert.llmTimeout == 0 && globalTimeout > 0)
        expert.llmTimeout = globalTimeout;
      if (expert.llmTemperature < 0 && globalTemperature >= 0)
        expert.llmTemperature = globalTemperature;
    }

    return !experts_.empty();
  } catch (const std::exception &) {
    return false;
  }
}

std::string AgentConfiguration::exportJson() const {
  std::lock_guard<std::mutex> lock(mutex_);

  json j;
  j["_comment"] = "CodePilot Expert Configuration - generated by "
                  "AgentConfiguration::exportJson()";
  j["_config_path"] = configPath_;

  json expertsArray = json::array();
  for (const auto &e : experts_) {
    json ej;
    ej["name"] = e.name;
    ej["description"] = e.description;
    ej["prompt_template"] = e.promptTemplate;
    ej["is_entry"] = e.isEntry;
    ej["context_isolation"] = e.contextIsolation;
    ej["context_template"] = e.contextTemplate;

    // 应用层
    if (!e.visibleTools.empty()) {
      ej["visible_tools"] = e.visibleTools;
    }

    // 系统层
    if (!e.canModifyPlan)
      ej["can_modify_plan"] = false;
    if (e.canWriteSummary)
      ej["can_write_summary"] = true;
    if (e.readGlobalActively)
      ej["read_global_actively"] = true;
    if (e.maxGlobalRounds > 0)
      ej["max_global_rounds"] = e.maxGlobalRounds;

    // LLM
    if (!e.llmProvider.empty())
      ej["_llm_provider"] = e.llmProvider;
    if (!e.llmModel.empty())
      ej["_llm_model"] = e.llmModel;
    if (e.llmTimeout > 0)
      ej["_llm_timeout"] = e.llmTimeout;
    if (e.llmTemperature >= 0)
      ej["_llm_temperature"] = e.llmTemperature;

    // 路由
    json rules = json::array();
    for (const auto &r : e.nextRules) {
      json rj;
      switch (r.conditionType) {
      case RouteRule::TagExists:
        rj["type"] = "tag_exists";
        break;
      case RouteRule::TagValueMatch:
        rj["type"] = "tag_value_match";
        break;
      case RouteRule::PlanState:
        rj["type"] = "plan_state";
        break;
      default:
        rj["type"] = "default";
        break;
      }
      rj["value"] = r.conditionValue;
      rj["route_to"] = r.routeTo;
      rj["priority"] = r.priority;
      rules.push_back(rj);
    }
    if (!rules.empty())
      ej["next_rules"] = rules;
    if (!e.onFailRoute.empty())
      ej["on_fail"] = e.onFailRoute;

    // 容量
    ej["max_internal_rounds"] = e.maxInternalRounds;
    ej["tool_timeout_seconds"] = e.toolTimeoutSeconds;

    expertsArray.push_back(ej);
  }
  j["experts"] = expertsArray;

  return j.dump(2);
}

bool AgentConfiguration::importJson(const std::string &jsonStr) {
  std::lock_guard<std::mutex> lock(mutex_);

  try {
    json j = json::parse(jsonStr);
    experts_.clear();

    if (j.contains("experts") && j["experts"].is_array()) {
      for (const auto &e : j["experts"]) {
        experts_.push_back(ExpertConfig::fromJson(e));
      }
    } else if (j.is_array()) {
      for (const auto &e : j) {
        experts_.push_back(ExpertConfig::fromJson(e));
      }
    } else {
      return false;
    }

    rawJson_ = jsonStr;
    return !experts_.empty();
  } catch (const std::exception &) {
    return false;
  }
}

bool AgentConfiguration::saveToFile() const { return saveToFile(configPath_); }

bool AgentConfiguration::saveToFile(const std::string &targetPath) const {
  std::lock_guard<std::mutex> lock(mutex_);

  std::string content = exportJson();
  std::ofstream file(targetPath);
  if (!file.is_open())
    return false;

  file << content;
  file.close();
  return true;
}

const ExpertConfig *
AgentConfiguration::getExpert(const std::string &name) const {
  std::lock_guard<std::mutex> lock(mutex_);
  for (const auto &e : experts_) {
    if (e.name == name)
      return &e;
  }
  return nullptr;
}

const ExpertConfig *AgentConfiguration::getEntryExpert() const {
  std::lock_guard<std::mutex> lock(mutex_);
  for (const auto &e : experts_) {
    if (e.isEntry)
      return &e;
  }
  return experts_.empty() ? nullptr : &experts_.front();
}

std::vector<std::string> AgentConfiguration::listExpertNames() const {
  std::lock_guard<std::mutex> lock(mutex_);
  std::vector<std::string> names;
  for (const auto &e : experts_) {
    names.push_back(e.name);
  }
  return names;
}

std::vector<ExpertConfig> AgentConfiguration::allExperts() const {
  std::lock_guard<std::mutex> lock(mutex_);
  return experts_;
}

bool AgentConfiguration::isReady() const {
  std::lock_guard<std::mutex> lock(mutex_);
  return !experts_.empty();
}

std::string AgentConfiguration::rawJson() const {
  std::lock_guard<std::mutex> lock(mutex_);
  return rawJson_;
}


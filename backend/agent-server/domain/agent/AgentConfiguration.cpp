bool AgentConfiguration::addExpert(const ExpertConfig &expert) {
  std::lock_guard<std::mutex> lock(mutex_);
  // 检查名称冲突
  for (const auto &e : experts_) {
    if (e.name == expert.name)
      return false;
  }
  experts_.push_back(expert);
  rebuildRawJson();
  return true;
}

bool AgentConfiguration::updateExpert(const std::string &name,
                                      const ExpertConfig &config) {
  std::lock_guard<std::mutex> lock(mutex_);
  for (auto &e : experts_) {
    if (e.name == name) {
      e = config;
      e.name = name; // 保持名称不变
      rebuildRawJson();
      return true;
    }
  }
  return false;
}

bool AgentConfiguration::patchExpert(const std::string &name,
                                     const std::string &jsonPatch) {
  std::lock_guard<std::mutex> lock(mutex_);
  try {
    json patch = json::parse(jsonPatch);
    if (!patch.is_object())
      return false;

    for (auto &e : experts_) {
      if (e.name != name)
        continue;

      // ---- 基本字段 ----
      if (patch.contains("description"))
        e.description = patch["description"].get<std::string>();
      if (patch.contains("prompt_template"))
        e.promptTemplate = patch["prompt_template"].get<std::string>();
      if (patch.contains("is_entry"))
        e.isEntry = patch["is_entry"].get<bool>();
      if (patch.contains("context_isolation"))
        e.contextIsolation = patch["context_isolation"].get<bool>();
      if (patch.contains("context_template"))
        e.contextTemplate = patch["context_template"].get<std::string>();

      // ---- 应用层 ----
      if (patch.contains("visible_tools") &&
          patch["visible_tools"].is_array()) {
        e.visibleTools.clear();
        for (const auto &t : patch["visible_tools"])
          e.visibleTools.push_back(t.get<std::string>());
      }

      // ---- 系统层 ----
      if (patch.contains("can_modify_plan"))
        e.canModifyPlan = patch["can_modify_plan"].get<bool>();
      if (patch.contains("can_write_summary"))
        e.canWriteSummary = patch["can_write_summary"].get<bool>();
      if (patch.contains("read_global_actively"))
        e.readGlobalActively = patch["read_global_actively"].get<bool>();
      if (patch.contains("max_global_rounds"))
        e.maxGlobalRounds = patch["max_global_rounds"].get<int>();

      // ---- LLM ----
      if (patch.contains("llm_provider"))
        e.llmProvider = patch["llm_provider"].get<std::string>();
      if (patch.contains("llm_model"))
        e.llmModel = patch["llm_model"].get<std::string>();
      if (patch.contains("llm_timeout"))
        e.llmTimeout = patch["llm_timeout"].get<int>();
      if (patch.contains("llm_temperature"))
        e.llmTemperature = patch["llm_temperature"].get<double>();

      // ---- 路由 ----
      if (patch.contains("next_rules") && patch["next_rules"].is_array()) {
        e.nextRules.clear();
        for (const auto &r : patch["next_rules"])
          e.nextRules.push_back(RouteRule::fromJson(r));
      }
      if (patch.contains("on_fail"))
        e.onFailRoute = patch["on_fail"].get<std::string>();

      // ---- 容量 ----
      if (patch.contains("max_internal_rounds"))
        e.maxInternalRounds = patch["max_internal_rounds"].get<int>();
      if (patch.contains("tool_timeout_seconds"))
        e.toolTimeoutSeconds = patch["tool_timeout_seconds"].get<int>();

      rebuildRawJson();
      return true;
    }
    return false;
  } catch (const std::exception &) {
    return false;
  }
}

bool AgentConfiguration::removeExpert(const std::string &name) {
  std::lock_guard<std::mutex> lock(mutex_);
  auto it =
      std::find_if(experts_.begin(), experts_.end(),
                   [&name](const ExpertConfig &e) { return e.name == name; });
  if (it == experts_.end())
    return false;
  experts_.erase(it);
  rebuildRawJson();
  return true;
}


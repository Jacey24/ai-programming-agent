bool AgentConfiguration::validate(const std::string &jsonStr,
                                  std::string &errorOut) {
  try {
    json j = json::parse(jsonStr);
    if (!j.contains("experts") || !j["experts"].is_array()) {
      errorOut = "缺少 experts 数组";
      return false;
    }
    for (size_t i = 0; i < j["experts"].size(); ++i) {
      const auto &e = j["experts"][i];
      if (!e.contains("name") || !e["name"].is_string()) {
        errorOut = "experts[" + std::to_string(i) + "] 缺少 name 字段";
        return false;
      }
      if (e.contains("next_rules") && e["next_rules"].is_array()) {
        for (size_t ri = 0; ri < e["next_rules"].size(); ++ri) {
          const auto &r = e["next_rules"][ri];
          if (!r.contains("route_to") || !r["route_to"].is_string()) {
            errorOut = "experts[" + std::to_string(i) + "].next_rules[" +
                       std::to_string(ri) + "] 缺少 route_to";
            return false;
          }
        }
      }
    }
    return true;
  } catch (const std::exception &ex) {
    errorOut = std::string("JSON 解析失败: ") + ex.what();
    return false;
  }
}

